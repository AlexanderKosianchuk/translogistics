<h3 style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:
1.0cm'><span lang=UK style='font-size:12.0pt;font-family:"Calibri","sans-serif"'>1
Предмет договору</span></h3>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:1.0cm'><span
lang=UK style='font-family:"Calibri","sans-serif"'>1.1 Даний договір регулює
взаємовідносини сторін, що виникають при організації перевезень вантажів в залізничному
рухомому складі й пов’язаних з цим послугах, та здійснення розрахунків за
надані послуги.</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:1.0cm'><span
lang=UK style='font-family:"Calibri","sans-serif"'>1.2 Експедитор згідно даного
Договору, який є дорученням Клієнта, на підставі заявок Клієнта, за його
рахунок та за винагороду (плату) організовує перевезення вантажів в
залізничному рухомому складі й пов’язані з цим послуги шляхом укладання
договорів з підприємствами, уповноваженими здійснювати перевезення або
організацію перевезень вантажів, підприємствами – власниками
(балансоутримувачами) залізничного рухомого складу, іншими юридичними особами,
а Клієнт сплачує  й приймає надані Експедитором послуги на умовах даного
договору.</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:1.0cm'><span
lang=UK style='font-family:"Calibri","sans-serif"'>&nbsp;</span></p>

<h3 style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:
27.0pt'><span lang=UK style='font-size:12.0pt;font-family:"Calibri","sans-serif"'>2
Права та обов'язки Сторін </span></h3>

<h3 style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:
27.0pt'><span lang=UK style='font-size:12.0pt;font-family:"Calibri","sans-serif";'>2.1 Сторони зобов'язані виконувати всі умови даного
Договору та мають право на взаємне отримання інформації, необхідної для
виконання ними своїх обов'язків за цим Договором.</span></h3>

<h3 style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:
45.0pt'><span lang=UK style='font-size:12.0pt;font-family:"Calibri","sans-serif";'>&nbsp;</span></h3>

<h3 style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:
27.0pt'><span lang=UK style='font-size:12.0pt;font-family:"Calibri","sans-serif"'>2.2
Обов'язки Клієнта:</span></h3>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:27.0pt'><span
lang=UK style='font-family:"Calibri","sans-serif";'>2.2.1 За 5 діб
до дати подачі вагонів відправляти Експедитору заявку на подачу вантажних
вагонів.</span></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif";'>2.2.2  До початку
здійснення перевезень узгоджувати з Експедитором  зміни в планах перевезення. </span></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif";'>2.2.3 Приймати вагони,
які подані за заявкою Клієнта. Відмова від прийняття вагонів допускається лише
у випадках, якщо виключається можливість використання  вагонів під перевезення
вантажу внаслідок їх технічної або комерційної непридатності. Непридатність
вагонів в комерційному та/або технічному відношенні повинна підтверджуватись
повідомленнями та актами форми ВУ-23, ВУ-25, ВУ-26 (технічні несправності) та форми
ГУ-23, оформленими згідно вимог Статуту залізниць України та Правил перевезень
вантажів залізничним транспортом (надалі Правил перевезень вантажів).</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:27.0pt'><span
lang=UK style='font-family:"Calibri","sans-serif";'>2.2.4
Використовувати надані вагони для перевезень вантажів по узгодженим планам і
заявкам.</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:27.0pt'><span
lang=UK style='font-family:"Calibri","sans-serif";'>2.2.5 Завчасно
інформувати Експедитора про переадресування вантажів із зазначенням
найменування вантажу, номерів вагонів та накладних, найменування
вантажовідправника, станції відправлення, станції й залізниці попереднього
призначення, найменування попереднього вантажоодержувача, найменування станції
та залізниці нового призначення, найменування та поштової адреси нового
вантажоодержувача, організації або посадової особи, якій довіряється здійснити
оформлення перевізних документів на вантаж, що переадресовується, а також
сплатити всі витрати, пов’язані з переадресуванням, та, при необхідності,
здійснити доплату за перевезення вантажу у переадресованих вагонах.</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:27.0pt'><span
lang=UK style='font-family:"Calibri","sans-serif";'>2.2.6
Забезпечувати належне заповнення перевізних документів вантажовідправниками. В
перевізних документах має бути вказаний код платника транспортних послуг,
наданий Експедитором, та найменування вантажовідправника.</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:27.0pt'><span
lang=UK style='font-family:"Calibri","sans-serif"'>2.2.7 У випадках,
передбачених законодавством, самостійно здійснювати страхування  вантажів,
організація перевезень яких здійснюється Експедитором за заявками Клієнта.</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:27.0pt'><span
lang=UK style='font-family:"Calibri","sans-serif"'>2.2.8 Дотримуватись
виконання норм вагонообігу, узгоджених Договорами про експлуатацію під’їзних
колій (Договорами про подачу та забирання вагонів), та не використовувати
вагони, як місця для зберігання вантажів.</span></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt;background:
white'><span lang=UK style='font-family:"Calibri","sans-serif"'>2.2.9 <span
style='letter-spacing:-.45pt'>Запобігати пошкодженню вагонів та забезпечувати
їх експлуатацію відповідно до Технічних умов навантаження і кріплення вантажів,
Правил перевезення вантажів залізничним транспортом та Правилами технічної
експлуатації залізниць України. </span></span></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:27.0pt'><span
lang=UK style='font-family:"Calibri","sans-serif"'>2.2.10 Для виконання
операцій з порожніми вагонами  здійснювати  передачу порожніх вагонів з
під’їзних колій на станції залізниць укрупненими групами або маршрутами.</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:27.0pt'><span
lang=UK style='font-family:"Calibri","sans-serif"'>2.2.11 Проводити оплату за
організацію перевезень та  послуги, пов'язані з перевезеннями,  оплату інших
затрат Експедитора, та винагороду (плату) Експедитору в порядку, передбаченому
даним Договором. Своєчасно забезпечити Експедитора транзитними коштами,
необхідними для виконання Доручення, а при витрачанні власних коштів
Експедитора в інтересах Клієнта, провести їх компенсацію.</span></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt;background:
white'><span lang=UK style='font-family:"Calibri","sans-serif";letter-spacing:
-.45pt'>2.2.12 Здійснювати навантаження та вивантаження вагонів<span
style=''>, відправлення вантажів за призначенням</span><span
style='color:#00B050'>, </span>а також відправлення в порожньому стані вагонів
за перевізними документами зі станцій, розташованих на залізницях інших держав.
</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:27.0pt'><span
lang=UK style='font-family:"Calibri","sans-serif"'>2.2.13 При здійсненні
перевезень вантажів за межі митного кордону України самостійно організовувати 
перевезення вантажів за кордоном, здійснювати плату <span style=''>за
час перебування кожної одиниці рухомого складу за кордоном України відповідно
до умов  даного Договору, сприяти своєчасному поверненню вагонів  із-за
кордону.</span></span></p>

<p class=MsoBodyTextIndent style='text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif";'>2.2.14 Самостійно
оформляти або організовувати оформлення перевізних документів і документів,
необхідних  для виконання митних та інших процедур по оформленню вантажу на
прикордонних станціях.</span></p>

<p class=MsoBodyTextIndent style='text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif";'>2.2.15 Негайно
інформувати Експедитора про всі обставини, які не залежать від Експедитора та
перешкоджають відправленню та просуванню вагонів.</span></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif";'>2.2.16 В терміни,
встановлені  Договором, підписувати акти звірки розра</span><span lang=UK
style='font-family:"Calibri","sans-serif"'>хунків, інші документи, які
потребують підпису Клієнта.</span></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>2.2.17 Вид та найменування вантажу,
пункти відправлення та призначення кожного вантажу вказуються в Заявці на
перевезення. Сторони домовилися, що оплата Клієнтом рахунку Експедитора,
виставленого відповідно до Заявки Клієнта, є свідченням узгодження Сторонами
умов, відображених у зазначених документах. Порядок зміни маршруту, виду
транспорту, вказівок Клієнта визначаються Заявкою.</span></p>

<p class=MsoNormal style='text-align:justify'><span lang=UK style='font-family:
"Calibri","sans-serif"'>&nbsp;</span></p>

<h3 style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:
27.0pt'><span lang=UK style='font-size:12.0pt;font-family:"Calibri","sans-serif"'>2.3
Клієнт має право:</span></h3>

<h3 style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:
27.0pt'><span lang=UK style='font-size:12.0pt;font-family:"Calibri","sans-serif";
'>2.3.1 Використовувати вагони відповідно до Правил
перевезення вантажів та умов Договору.</span></h3>

<p class=MsoBodyTextIndent style='text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>2.3.2 Від<span style=''>кликати</span>
надану Експедитору Заявку не пізніше, ніж за 4 доби до дати подачі вагонів.</span></p>

<p class=MsoBodyTextIndent style='text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>У разі відмови Клієнта  від вагонів
пізніше вказаного терміну, Клієнт компенсує Експедитору фактичні витрати, які
виникли у зв’язку з такою відмовою, що підтверджується розрахунком витрат та
пред’явленим рахунком.</span></p>

<h3 style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:
27.0pt'><span lang=UK style='font-size:12.0pt;font-family:"Calibri","sans-serif";
'>2.3.3 Ініціювати внесення змін та доповнень до Договору.</span></h3>

<h3 style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:
27.0pt'><span lang=UK style='font-size:12.0pt;font-family:"Calibri","sans-serif";
'>2.3.4 Вимагати від Експедитора  належного виконання
прийнятих на себе зобов'язань у відповідності з Договором.</span></h3>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>2.3.5 Відмовитися від прийняття
вагонів у випадках, якщо виключається можливість використання вагонів під
перевезення вантажу внаслідок їх технічної або комерційної непридатності, якщо
непридатність вагонів в комерційному та/або технічному відношенні підтверджена <span
style=''>повідомленнями та актами форми ВУ-23, ВУ-25, ВУ-26
(технічні несправності) та ГУ-23, оформленими згідно вимог Статуту залізниць
України та Правил перевезень вантажів залізничним транспортом</span>.</span></p>

<h3 style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:
27.0pt'><span lang=UK style='font-size:12.0pt;font-family:"Calibri","sans-serif"'>2.4
Обов'язки Експедитора:</span></h3>

<p class=MsoNormal style='text-align:justify;text-indent:21.3pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>2.4.1 Приймати до розгляду та
виконання заявки Клієнта на організацію перевезень вантажів та на  понадпланові
(позапланові)  перевезення, виходячи з поточних можливостей Державного
підприємства «Український транспортно-логістичний центр», підприємств-власників
(балансоутримувачів) залізничного рухомого складу, залізниць, <span
style=''>інших юридичних осіб</span> в здійсненні перевезень
вантажів.</span></p>

<p class=MsoNormal style='text-align:justify;text-indent:21.3pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>2.4.2 Організовувати відповідно до
наданих Клієнтом  Заявок на перевезення вантажів Клієнта  залізничним
транспортом та надавати інші послуги, що пов’язані з перевезенням вантажів,
шляхом укладання договорів з підприємствами, вповноваженими здійснювати
перевезення або організацію перевезень вантажів, підприємствами – власниками
(балансоутримувачами) залізничного рухомого складу, іншими юридичними особами.</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:21.3pt'><span
lang=UK style='font-family:"Calibri","sans-serif"'>2.4.3 Своєчасно здійснювати
розрахунки з особами,  з якими у Експедитора укладені угоди на виконання
доручень Клієнта</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:21.3pt'><span
lang=UK style='font-family:"Calibri","sans-serif"'>2.4.4 Не пізніше 15-го числа
місяця, наступного за місяцем надання послуг, надавати Клієнту рахунки,
податкові накладні  за надані послуги та Звіти Експедитора про виконання
доручень Клієнта.</span></p>

<p class=MsoBodyTextIndent><span lang=UK style='font-family:"Calibri","sans-serif"'>&nbsp;</span></p>

<h3 style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:
27.0pt'><span lang=UK style='font-size:12.0pt;font-family:"Calibri","sans-serif"'>2.5
Експедитор</span><span lang=UK style='font-size:12.0pt;font-family:"Calibri","sans-serif"'>
</span><span lang=UK style='font-size:12.0pt;font-family:"Calibri","sans-serif"'> має
право:</span></h3>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>2.5.1 Відмовити або призупинити
виконання узгодженої Заявки Клієнта в разі відсутності можливостей у Державного
підприємства «Український транспортно-логістичний центр», підприємств –
власників (балансоутримувачів) залізничного рухомого складу, залізниць <span
