<p class=MsoNormal style='text-align:justify;text-indent:26.95pt'><span
lang=UK style='font-family:"Calibri","sans-serif"'>4.2</span><span lang=UK
style='font-family:"Calibri","sans-serif"'> Клієнт несе майнову
відповідальність за неправильно оформлені перевізні <span style=''>(неправильно
зазначені масу, кількість місць вантажу, його назву, код та адресу одержувача)
та супровідні</span> документи, відмову від наданих вагонів і відсутність
вантажоодержувача у місці призначення. У випадку оформлення залізничних
накладних та супровідних документів з порушенням вимог щодо їх оформлення, що
викликало затримку при перевезенні, Клієнт відшкодовує всі транспортні витрати,
а також витрати, що пов’язані зі зберіганням вантажу, штрафи <span
style=''>та інші витрати, що виставлені підприємствами залізничного
транспорту.</span></span></p>

<p class=MsoNormal style='text-align:justify;text-indent:26.95pt'><span
lang=UK style='font-family:"Calibri","sans-serif"'>4.3 Клієнт несе
відповідальність за використання наданих експедитором кодів платника
транспортних послуг, та у випадку використання кодів без згоди Експедитора
сплачує по всім документам, в яких вказаний наданий Експедитором Клієнту код
платника транспортних послуг на умовах та в порядку, передбачених розділом 3
даного Договору, та штраф в розмірі 50% провізної плати.</span></p>

<p class=MsoNormal style='text-align:justify;text-indent:26.95pt'><span
lang=UK style='font-family:"Calibri","sans-serif"'>4.4 У випадку прострочення
строків оплати, передбачених даним Договором, Клієнт сплачує Експедитору
неустойку в розмірі подвійної облікової ставки НБУ від вартості простроченого
платежу за кожен день прострочення.</span></p>

<p class=MsoNormal style='text-align:justify;text-indent:26.95pt'><span
lang=UK style='font-family:"Calibri","sans-serif"'>4.5 Експедитор не несе
відповідальності за стан, кількість місць, вагу, упаковку вантажів, організація
перевезень яких здійснюється за даним Договором, а також за дії третіх осіб,
пов’язаних з організацією перевезень вантажів. </span></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:26.95pt'><span
lang=UK style='font-family:"Calibri","sans-serif"'>4.6 Сплата штрафних санкцій
не звільняє Сторони від виконання зобов'язань за цим Договором. </span></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:26.95pt'><span
lang=UK style='font-family:"Calibri","sans-serif"'>4.7 В разі порушення
сторонами умов даного договору, винна сторона відшкодовує  добросовісній
стороні нанесені збитки згідно чинного законодавства України.</span></p>

<p class=MsoBodyTextIndent style='margin-left:18.0pt;text-indent:45.0pt'><b><span
