<p class=MsoNormal style='text-align:justify;text-indent:45.0pt'><b><span
lang=UK style='font-family:"Calibri","sans-serif"'>5 Обставини непереборної
сили</span></b></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>5.1 Сторони звільняються від
відповідальності за невиконання або неналежне виконання зобов'язань за цим
Договором у разі виникнення обставин непереборної сили (форс–мажор), які не
існували під час укладання договору та виникли поза волею сторін, зокрема:
аварія, катастрофа, стихійне лихо, епідемія, епізоотія, землетруси, повені,
пожежі, стихійні лиха, заборонні дії влади, військові дії, страйки, крім
страйків на підприємствах Сторін, тощо. </span></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>5.2 Сторона, що не може виконувати
зобов'язання за цим Договором унаслідок дії обставин непереборної сили, повинна
не пізніше 10 календарних днів з моменту їх виникнення повідомити про це іншу
сторону у письмовій формі. </span></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>5.3 Доказом виникнення обставин
непереборної сили та строку їх дії є відповідні документи, які видаються
Торгово-промисловою палатою України або країни, на території якої мали місце
обставин непереборної сили.</span></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>5.4 Виникнення обставин непереборної
сили не є підставою для відмови Клієнта від сплати за послуги, надані до
виникнення вказаних обставин.</span></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>5.5 У разі якщо форс-мажорні
обставини будуть тривати понад три місяці, кожна Сторона має право в
односторонньому порядку розірвати цей Договір без відшкодування іншій Стороні
збитків, але за умови здійснення взаємних розрахунків за послуги, надані на
дату розірвання цього Договору.</span></p>

<p class=MsoNormal style='text-align:justify;text-indent:45.0pt'><b><span
lang=UK style='font-family:"Calibri","sans-serif"'>6 Вирішення спорів</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:27.0pt'><span
lang=UK style='font-family:"Calibri","sans-serif"'>6.1 Спірні питання з
виконання умов цього Договору вирішуються Сторонами шляхом переговорів, а у
разі недосягнення домовленості - у претензійно-позовному порядку. </span></p>

<p class=MsoBodyTextIndent style='text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>6.2 Якщо згоди не буде досягнуто,
спір передається на розгляд до господарського суду, у порядку, встановленому
Господарським процесуальним кодексом України.</span></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>&nbsp;</span></p>

<p class=MsoBodyTextIndent><b><span lang=UK style='font-family:"Calibri","sans-serif"'>7
Застереження про конфіденційність</span></b></p>

<p class=MsoBodyTextIndent style='text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>7.1 Сторони погодилися, що текст
Договору, будь-які матеріали, інформація та відомості, які стосуються Договору,
є конфіденційними і не можуть передаватися іншим особам без попередньої
письмової згоди іншої Сторони Договору, крім випадків, коли таке передавання
пов’язане з одержанням офіційних дозволів, документів для виконання Договору,
або сплати податків, інших обов’язкових платежів, а також у випадках,
передбачених чинним законодавством, яке регулює зобов’язання Сторін  Договору.</span></p>

<p class=MsoBodyTextIndent style='text-indent:35.45pt'><b><span lang=UK
style='font-family:"Calibri","sans-serif"'>8 Інші умови</span></b></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>8.1 <b> </b></span><span lang=UK
style='font-family:"Calibri","sans-serif"'>Сторона Договору не може передавати
свої права і обов’язки по цьому Договору іншим особам без письмової згоди іншої
Сторони.</span></p>

<p class=MsoBodyTextIndent style='text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>8.2<b> </b></span><span lang=UK
style='font-family:"Calibri","sans-serif"'>Усі зміни, додатки і доповнення до
цього Договору є його невід’ємною частиною, повинні бути викладені у письмовій
формі та підписані уповноваженими представниками Сторін і скріплені печатками.</span></p>

<p class=MsoBodyTextIndent style='text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>8.3<b> </b>Даний Договір складено у
двох примірниках, які мають однакову юридичну силу, по одному для кожної із
Сторін. </span></p>

<p class=MsoBodyTextIndent style='text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>8.4  У разі зміни реквізитів
підприємства, фактичної адреси, інших змін, які можуть перешкоджати виконанню
зобов’язань по цьому Договору, Сторони зобов’язані повідомити про це один
одного не пізніше 15 календарних днів з моменту виникнення таких змін.</span></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>8.5  Сторони приймають документи,
передані факсимільним зв'язком або засобами електронного зв'язку (електронною
поштою), при підтвердженні їх оригіналами протягом 10 (десяти) робочих днів. До
отримання оригіналів факсимільні та електронні копії вважаються такими, що
мають юридичну силу, крім первинних документів, які підтверджують здійснення
господарських операцій та служать засобом для підтвердження операцій в
бухгалтерському обліку.</span></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>8.6 Діяльність Сторін
регламентується чинним законодавством України, Статутом залізниць України,
СМГС, національним транспортним правом і нормативними актами, що стосуються
перевезення вантажів залізничним транспортом. У всьому іншому, що не
передбачено цим Договором, Сторони керуються чинним законодавством України. </span></p>

<p class=MsoNormal style='text-align:justify;text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>8.7 Сторони є платниками податку на
прибуток на загальних умовах згідно чинного законодавства України.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span lang=UK
style='font-family:"Calibri","sans-serif"'>9 Термін дії Договору</span></b></p>

<p class=MsoBodyTextIndent style='text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>9.1 </span><span lang=UK
style='font-family:"Calibri","sans-serif"'>Договір набирає чинності з дати
підписання Сторонами та діє до 31.12.2014 року включно.</span></p>

<p class=MsoBodyTextIndent style='text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>9.2 Договір вважається пролонгованим
на кожен наступний календарний рік, якщо жодна зі сторін не менш, ніж за 30
днів до закінчення його дії не повідомить іншу Сторону в письмовій формі про
припинення дії договору.</span></p>

<p class=MsoBodyTextIndent style='text-indent:27.0pt'><span lang=UK
style='font-family:"Calibri","sans-serif"'>9.3 Сторони мають право розірвати
