<p class=MsoNormal style='margin-right:-5.8pt;text-align:justify;text-indent:
1.0cm'><span lang=UK style='font-family:"Calibri","sans-serif"'>3.1 Клієнт для
організації перевезень вантажів і пов’язаних з цим послуг та для відшкодування
витрат Експедитора здійснює авансові платежі. Авансові платежі мають бути здійснені
до початку кожного місяця здійснення перевезень. Авансові платежі здійснюються
відповідно до вартості перевезень, інших послуг та витрат Експедитора, що
плануються впродовж кожного місяця надання послуг. За згодою Експедитора
можлива оплата послуг (їхньої частини) з організації перевезень вантажів за
фактом їх виконання впродовж 3-х календарних днів з моменту надання рахунку
Експедитором.</span></p>

<p class=MsoNormal style='margin-right:-5.8pt;text-align:justify'><span
