<p class=MsoNormal style='margin-right:-.05pt;text-align:justify;text-indent:
1.0cm'><span lang=UK style='font-family:"Calibri","sans-serif"'>3.2 Вартість
послуг з організації перевезень вантажів визначається в розмірі тарифів,
встановлених чинним законодавством України для перевезення вантажів в вагонах 
залізничним транспортом у межах  України та пов’язані з ними послуги із
використанням коефіцієнтів на дату відправлення вантажу для вагонів парку
залізниць з коригуючим коефіцієнтом  у залежності від відстані перевезень (до
300км - К=1,11, 300км та більше К =1,14) та складається з тарифу на перевезення
вантажу, встановленого для вагонів власного парку, розрахункової вагонної
складової  та послуг виконавця.  Вказана ставка містить в собі вартість
залізничного тарифу за перевезення вагонів, як в навантаженому, так і в
порожньому стані, що стягується залізницями, вартість послуг Державного
підприємства «Український транспортно-логістичний центр»;</span></p>

<p class=MsoNormal style='margin-right:-.05pt;text-align:justify;text-indent:
1.0cm'><span lang=UK style='font-family:"Calibri","sans-serif"'>Розмір плати за
знаходження кожної одиниці вагонів за межами митного кордону України при
виконанні Заявок Клієнта в розмірі.-додаткових зборів,пов’язаних з перевезенням
та встановлених «Збірником тарифів на перевезення вантажів залізничним
транспортом у межах України», інших витрат Експедитора, які нараховані йому при
виконанні заявок Клієнта Державним підприємством « Український транспортно –
логістичний центр». Податок на додану вартість нараховується у
випадках,визначених чинним законодавством. </span></p>

<p class=MsoNormal style='margin-right:-5.8pt;text-align:justify;text-indent:
1.0cm'><span lang=UK style='font-family:"Calibri","sans-serif"'>Клієнт
самостійно організує розрахунки з підприємствами залізничного транспорту за
надані послуги (подача та забирання вагонів, плата за користування вагонами у
випадку простою вагонів  на коліях залізниць з вини вантажоодержувача,
вантажовідправника, плата за користування вагонами на під’їзних коліях
власників під’їзних колій та інше). Розмір плати за перебування вагону у
завантаженому та порожньому стані за межами митного кордону України при
організації перевезень з/в країн/і СНД та Балтії згідно заявок Клієнта складає
за кожну одиницю рухомого складу 25 (двадцять п’ять) швейцарських франків за
добу без урахування ПДВ,що перераховані у національну валюту України за курсом
встановленого Національним банком України на день здійснення Клієнтом оплати</span></p>

<p class=MsoNormal style='margin-right:-.05pt;text-align:justify;text-indent:
21.3pt'><span lang=UK style='font-family:"Calibri","sans-serif"'>3.3 Винагорода
(плата) Експедитору встановлюється в розмірі 2,75 % від вартості організації
перевезень  вантажів  (без ПДВ, який нараховується у випадках, визначених
чинним законодавством України) та сплачується Клієнтом впродовж 3-х робочих
днів після надання послуг на підставі рахунків Експедитора або утримується
Експедитором самостійно з авансових платежів Клієнта.</span></p>

<p class=MsoNormal style='margin-right:-5.8pt;text-align:justify;text-indent:
1.0cm'><span lang=UK style='font-family:"Calibri","sans-serif"'>При укладанні
Експедитором правочинів з іншими юридичними особами, що необхідні для виконання
заявок Клієнта, за вартістю меншою, ніж вказано в  даному розділі Договору,
різниця є додатковою винагородою (платою) Експедитора.</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;text-align:justify;text-indent:21.3pt'><span
lang=UK style='font-family:"Calibri","sans-serif"'>3.4 Не рідше одного разу на
